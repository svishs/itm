    # Дано целое число K. Вывести строку-описание оценки, соответствующей числу K (1 — «плохо», 2 — «неудовлетворительно», 3 — «удовлетворительно», 4 — «хорошо», 5 — «отлично»). Если K не лежит в диапазоне 1–5, то вывести строку «ошибка».
    rating = {
        0: "ошибка",
        1: "плохо",
        2: "неудовлетворительно",
        3: "удовлетворительно",
        4: "хорошо",
        5: "отлично",
    }
    try:
        pick = int(input("Введите число от 1 до 5 "))
    except ValueError:
        raise SystemExit("Ошибка ввода. Завершение")

    print(f"Вы сделали выбор = {rating.get(pick,rating[0])}")

